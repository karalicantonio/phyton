mood = input("Kako se osjecas danas? ")

if mood == "sretno":
    print("Predivno , jako mi je drago kad si sretan!")
elif mood == "nervozno":
    print("Duboko udahni i sve ce biti uredu.")
elif mood == "tuzno":
    print("Ma daj se ne sekiraj!")
elif mood == "uzbudeno":
    print("Woohoo!")
elif mood == "opusteno":
    print("Chillaj onda samo")
else:
    print("Ne kontam te.")
